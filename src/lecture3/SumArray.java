package Lecture3;

import java.util.Scanner;

public class SumArray {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int arraysA = sc.nextInt();
        int arraysB = sc.nextInt();
        int sum = arraysA + arraysB;
        System.out.println("sum of arraysA and arrays B is =" + sum);
    }

}
